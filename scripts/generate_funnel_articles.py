def load_aggregate_stats():
    """Load aggregate market stats from all hh-cache files."""
    total_vacancies = 0
    all_salaries = []
    for f in HH_CACHE_DIR.glob("*.json"):
        with open(f) as fh:
            data = json.load(fh)
        grades = data.get("grades", {})
        for g in grades.values():
            total_vacancies += g.get("russia", {}).get("vacancies", 0)
            s = g.get("moscow", {}).get("salaryMedian")
            if s:
                all_salaries.append(s)
    median_salary = sorted(all_salaries)[len(all_salaries)//2] if all_salaries else 200000
    return total_vacancies, median_salary


